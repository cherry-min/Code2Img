const model = defineModel<string>();
const searchTerm = ref("");
const open = ref(false);
const limit = ref(80);
const list = computed(() =>
  icons
    .filter((icon) =>
      icon.toLowerCase().includes(searchTerm.value.toLowerCase()),
    )
    .slice(0, limit.value),
);
const limitTimeout = ref();
